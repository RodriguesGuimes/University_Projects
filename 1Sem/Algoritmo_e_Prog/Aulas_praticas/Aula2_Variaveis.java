public class Aula2_Variaveis {
    public static void main (String [] args){
        int a = 19, b = 4;

        int soma = a + b;
        int subtracao = a - b;
        int multiplicacao = a * b;
        int divisao = a / b;
        int resto = a % b;

        
        double x = 7.5, y = 0.25;

        double somaDouble = x + y;
        double subtracaoDouble = x - y;
        double multiplicacaoDouble = x * y;
        double divisaoDouble = x / y;


        System.out.println("soma = " + soma);
        System.out.println("subtracao = " + subtracao);
        System.out.println("multiplicacao = " + multiplicacao);
        System.out.println("divisao = " + divisao);
        System.out.println("resto = " + resto);

        System.out.println("Agora vamos trabalhar com as variaveis double");

        System.out.println("somaDouble = " + somaDouble);
        System.out.println("subtracaoDouble = " + subtracaoDouble);
        System.out.println("multiplicacaoDouble = " + multiplicacaoDouble);
        System.out.println("divisaoDouble = " + divisaoDouble);
    }
}
